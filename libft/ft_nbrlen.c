#include "libft.h"

size_t	ft_nbrlen(int nb)
{
	size_t	len;

	len = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
	{
		len++;
		nb *= -1;
	}
	while (nb)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
