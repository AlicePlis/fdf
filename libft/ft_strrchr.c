#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *s2;

	s2 = NULL;
	if ((char)c == '\0')
	{
		while (*s != '\0')
			s++;
		return ((char*)s);
	}
	while (*s)
	{
		if (*s == (char)c)
			s2 = (char*)s;
		s++;
	}
	return (s2);
}
